// relational operators 

public class third {
    public static void main(String args[]) {
        int a = 4;
        int b = 6;
        int c = 9;
        int d = 9;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(c > d);
        System.out.println(c < d);
    }
}
