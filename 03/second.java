// unary operators

public class second {
    public static void main(String args[]) {
        int a = 9;
        int b = 5;

        System.out.println(a++);
        System.out.println(b--);

        int c = 6;
        int d = 4;

        // pre-increment
        ++c;
        System.out.println(c);

        // post-increment
        d++;
        System.out.println(d);
    }
}
