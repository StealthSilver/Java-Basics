// switch case statement 

public class tenth {
    public static void main(String args[]) {

        int number = 2;

        switch (number) {
            case 1:
                System.out.println("coffee");
                break; // this stops the code after case 1
            case 2:
                System.out.println("tea");
                break;
            case 3:
                System.out.println("milk");
                break;
            default:
                System.out.println("nothing");
        }
    }
}
