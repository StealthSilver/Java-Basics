// explicit converting the char data type 

public class fifteenth {
    public static void main(String args[]) {
        char ch = 'a';
        char ch2 = 'b';

        int num1 = ch;
        int num2 = ch2;

        // the ASCII values of the characters will be given
        System.out.println(num1);
        System.out.println(num2);
    }
}
